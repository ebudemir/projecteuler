# The smallest positive number that is evenly divisible by all of the numbers from 1 to 20 is found by
# taking the least common multiple (LCM) of the numbers from 1 to 20. To do this, take the highest powers
# of the prime numbers that appear in the factorizations of the numbers 1 through 20.
#
# The needed primes and their highest powers are:
# 2^4 = 16
# 3^2 = 9
# 5^1 = 5
# 7^1 = 7
# 11^1 = 11
# 13^1 = 13
# 17^1 = 17
# 19^1 = 19
#
# Multiply these together:
# 16 × 9 = 144
# 144 × 5 = 720
# 720 × 7 = 5040
# 5040 × 11 = 55440
# 55440 × 13 = 720720
# 720720 × 17 = 12252240
# 12252240 × 19 = 232792560
#
# Answer: 232792560
