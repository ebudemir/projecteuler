# Sum of the first 100 natural numbers:
# 1 + 2 + 3 + … + 100 = 100 × 101 ÷ 2 = 5050
#
# Square of the sum:
# 5050² = 25,502,500
#
# Sum of the squares of the first 100 natural numbers:
# 1² + 2² + 3² + … + 100² = 100 × 101 × 201 ÷ 6 = 338,350
#
# Difference:
# 25,502,500 − 338,350 = 25,164,150
#
# Answer: 25,164,150
