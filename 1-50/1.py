#
# Sum of multiples of 3 below 1000:
# There are 333 multiples.
# Sum = 3 * (333 * 334) / 2 = 166833
#
# Sum of multiples of 5 below 1000:
# There are 199 multiples.
# Sum = 5 * (199 * 200) / 2 = 99500
#
# Sum of multiples of 15 below 1000 (to avoid double counting):
# There are 66 multiples.
# Sum = 15 * (66 * 67) / 2 = 33165
#
# Final sum = 166833 + 99500 âˆ’ 33165 = 233168
#
# So, the sum of all multiples of 3 or 5 below 1000 is 233168.
